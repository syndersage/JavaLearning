package com.lessons.lesson03;

public class Example3 {
    public static void main(String[] args) {
        int v;
        float x;
        v = 10;
        x = 10.0f;
        System.out.println("Original value of X: " + x);
        System.out.println("Original value of V: " + v);
        System.out.println();
        v = v / 4;
        x = x / 4;
        System.out.println("Changed value of X: " + x);
        System.out.println("Changed value of V: " + v);
    }
}
